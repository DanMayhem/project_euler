#file parsers
def get_sudoku_from_file(fn):
	with open(fn, "rtU") as f:
		puzzle=""
		for line in f:
			if line[0]=='G':#start of a new puzzle
				if len(puzzle)==81:
					yield puzzle
				puzzle = ""
			else:
				puzzle += line
	if len(puzzle)==81:
		yield puzzle


